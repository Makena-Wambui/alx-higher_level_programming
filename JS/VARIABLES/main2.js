#!/usr/bin/node
// Creating variables is declaring them
// Use the keyword let plus the name you want to give the variable

let myName;
let myAge;

// These variables are empty containers.
// They currently have no value.
// So undefined is returned.
// If the variable does not exist, then you get an error message.

// Initializing a variable with a value.
myName = "Alice";
myAge = 70;
console.log(`${myName}`);
console.log(`${myAge}`);
// console.log(`${scoobydoo}`);

// Declare and initialize simultaneously.
let myPet = "Levine";
console.log(`${myPet}`);
