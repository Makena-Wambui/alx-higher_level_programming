// dynamic typing
// JS is a dynamically typed language.
// unlike other languages, you do not need to specify what datatype a variable will contain.
// if you declare a variable, and give it a value enclosed in quotes,
// then that variable will be treated as a string
// use the typeof operator: returns the data type of the variable

let myNum = '40';
myNum = 40;
